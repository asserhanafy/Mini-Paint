    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        String positionX = inputPositionX.getText().strip();
        String positionY = inputPositionY.getText().strip();
        String lengthStr = inputLength.getText().strip();
        String widthStr = inputWidth.getText().strip();
        if (positionX.equals("") || positionY.equals("") || lengthStr.equals("") || widthStr.equals("") || color == null) {
            JOptionPane.showMessageDialog(this, "Some fields are empty!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Point position = new Point();
                position.x = Integer.parseInt(positionX);
                position.y = Integer.parseInt(positionY);
                double length = Double.parseDouble(lengthStr);
                double width = Double.parseDouble(widthStr);
                if (position.x < 0 || position.y < 0 || length < 0 || width < 0) {
                    throw new Exception("You should enter valid numbers!");
                } else if (position.x > canvas.getWidth() || position.y > canvas.getHeight()) {
                    throw new Exception("Coordinates out of bound! [length=425,width=250]");
                }
                Rectangle rectangle = new Rectangle(length, width);
                rectangle.setPosition(position);
                rectangle.setProperties(Map.of("length", length, "width", width));
                rectangle.setColor(color);
                rectangle.setFillColor(fillColor);
//                rectangle.draw(canvas.getGraphics());
                paint.addShape(rectangle);
                successful = true;
                this.dispose();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "You should enter valid numbers!", "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_createActionPerformed

